package source;

public class Transfer {
	private int no;
	private String type;
	private String date;
	private String hostBank;
	private String hostBankAccount;
	private String targetBank;
	private String targetBankAccount;
	private int balance;
	private String targetShow;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getHostBank() {
		return hostBank;
	}
	public void setHostBank(String hostBank) {
		this.hostBank = hostBank;
	}
	public String getHostBankAccount() {
		return hostBankAccount;
	}
	public void setHostBankAccount(String hostBankAccount) {
		this.hostBankAccount = hostBankAccount;
	}
	public String getTargetBank() {
		return targetBank;
	}
	public void setTargetBank(String targetBank) {
		this.targetBank = targetBank;
	}
	public String getTargetBankAccount() {
		return targetBankAccount;
	}
	public void setTargetBankAccount(String targetBankAccount) {
		this.targetBankAccount = targetBankAccount;
	}
	public int getBalance() {
		return balance;
	}
	public void setBalance(int balance) {
		this.balance = balance;
	}
	public String getTargetShow() {
		return targetShow;
	}
	public void setTargetShow(String targetShow) {
		this.targetShow = targetShow;
	}
	
	
}
